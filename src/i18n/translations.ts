export type LanguageCode = keyof typeof translations;

export function getTranslation(lang: string, key: string): string {
  const langKey = lang as LanguageCode;
  const translation = translations[langKey];
  if (!translation) {
    // 如果语言不存在，返回中文翻译
    return translations.zh_cn[key as keyof typeof translations.zh_cn] || key;
  }
  return translation[key as keyof typeof translation] || translations.zh_cn[key as keyof typeof translations.zh_cn] || key;
}

